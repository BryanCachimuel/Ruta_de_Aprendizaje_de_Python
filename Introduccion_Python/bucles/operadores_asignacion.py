"""
Operadores de Asignación
"""

# operador de asignación = 
a = 10
a = a + 5
print(a)

# forma simplificada con el operador  += -> suma en asignación
b = 125
b += 10
print(b)

# operador  -= -> resta en asignación
c = 47
c -= 2
print(c)

# operador  *= -> multiplicación en asignación
d = 20
d *= 4
print(d)

# operador  /= -> división en asignación
e = 144
e /= 4
print(e)

# operador %= -> modulo en asignación
f = 15
f %= 2
print(f)

# operador **= -> potencia en asignación
g = 8
g **= 3
print(g)

# incremento y decremento no existe en python
# h = 25
# print(h++)
# print(h--)

