numero1 = float(input("Ingrese un primer número: "))
numero2 = float(input("Ingrese un segundo número: "))

cociente = round((numero1 / numero2),2)
reciduo = numero1 % numero2

print(f'La división entre los números {numero1} y {numero2} tiene como resultado como cociente {cociente} y residuo {reciduo}')
